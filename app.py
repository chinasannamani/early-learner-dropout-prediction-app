# HELPER — build input dict from form values for a given Term_Stage
# ══════════════════════════════════════════════════════════════════════════════
def build_input_dict(term_stage, gender, age, edu_level, class_grade,
                     acad_year, school_type, income, parent_edu, distance,
                     attendance, has_textbooks, bullying,
                     t1, t2, t3,
                     ca1=0.0, midterm=0.0, exam=0.0, subs_failed=-1):
    """
    Construct the feature dict that matches the v4 preprocessor.
    Raw CA1/MidTerm/Exam scores are EXCLUDED — only engineered features pass in.
    """
    # Availability flags driven by stage
    ca1_avail     = 1 if term_stage >= 2 else 0
    midterm_avail = 1 if term_stage >= 3 else 0
    exam_avail    = 1 if term_stage >= 4 else 0

    # Derived current-term features
    current_score  = 0.0
    max_possible   = 0.0
    if term_stage == 1:
        current_score = 0.0
        max_possible  = 0.0
        perf_pct      = -1.0          # sentinel for NaN (Stage 1)
    elif term_stage == 2:
        current_score = ca1
        max_possible  = 20.0
        perf_pct      = ca1 / 20.0
    elif term_stage == 3:
        current_score = ca1 + midterm
        max_possible  = 40.0
        perf_pct      = (ca1 + midterm) / 40.0
    else:  # stage 4
        current_score = ca1 + midterm + exam
        max_possible  = 100.0
        perf_pct      = (ca1 + midterm + exam) / 100.0

    return {
        "Academic_Year":             acad_year,
        "Gender":                    gender,
        "Age":                       age,
        "Education_Level":           edu_level,
        "Class_Grade":               class_grade,
        "School_Type":               school_type,
        "Household_Income_Level":    income,
        "Parent_Guardian_Education": parent_edu,
        "Distance_to_School_km":     distance,
        "Attendance_Rate":           attendance,
        "Has_Textbooks":             has_textbooks,
        "Bullying_Flag":             bullying,
        "Term_Grade_Prev1":          t1,
        "Term_Grade_Prev2":          t2,
        "Term_Grade_Prev3":          t3,
        "Term_Stage":                term_stage,
        "CA1_Available":             ca1_avail,
        "MidTerm_Available":         midterm_avail,
        "Exam_Available":            exam_avail,
        "Current_Score_So_Far":      current_score,
        "Max_Possible_So_Far":       max_possible,
        "Performance_Pct_So_Far":    perf_pct,
        "Subjects_Failed":           subs_failed,
    }


